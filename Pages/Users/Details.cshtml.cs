using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using Final.Models;

namespace FinalProject.Pages.Users
{
    public class DetailsModel : PageModel
    {
        private readonly Final.Models.WorkoutContext _context;

        public DetailsModel(Final.Models.WorkoutContext context)
        {
            _context = context;
        }

      public new User User { get; set; } = default!; 
      

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null || _context.Users == null)
            {
                return NotFound();
            }

            var user = await _context.Users.Include(u => u.Workouts).FirstOrDefaultAsync(m => m.UserId == id);
            if (user == null)
            {
                return NotFound();
            }
            else 
            {
                User = user;
            }
            return Page();
        }
    }
}
